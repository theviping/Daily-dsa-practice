package Basic_Cheeze;

public class arithmeticOperationOnInt {
    public static void main(String[] args) {
        int a=20;
        int b=15;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a/b);

        double x=20,y=10;
        System.out.println("Double");
        System.out.println(x+y);
        System.out.println(x%y);
        System.out.println(x*y);
        System.out.println(x-y);
    }
}
