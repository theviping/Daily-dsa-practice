package Strings_go;

public class SubStringOfString {
    public static void main(String[] args) {
        String s="Vipin";
        System.out.println(s.substring(0,2));//index 0, 1 print krega
        System.out.println(s.substring(0,s.length()-2));//last se 2 nhi print hoga
        //substring lagatar hota hai;

    }
}
